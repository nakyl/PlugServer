package com.plugserver.controller;

public class PlugsAndStatus {

    private String statusPlug;
    private String plugSerial;

    public String getStatusPlug() {
        return statusPlug;
    }

    public void setStatusPlug(String statusPlug) {
        this.statusPlug = statusPlug;
    }

	/**
	 * @return the plugSerial
	 */
	public String getPlugSerial() {
		return plugSerial;
	}

	/**
	 * @param plugSerial the plugSerial to set
	 */
	public void setPlugSerial(String plugSerial) {
		this.plugSerial = plugSerial;
	}

}
